# Lista de perguntas
perguntas = [
    {"pergunta": "Qual a capital de Portugal?", "resposta": "Lisboa"},
    {"pergunta": "Quanto √© 2 + 2?", "resposta": "4"},
    {"pergunta": "Cor prim√°ria misturada com azul para formar roxo?", "resposta": "Vermelho"},
    {"pergunta": "Qual √© o maior oceano da Terra?", "resposta": "Pac√≠fico"},
    {"pergunta": "Python √© uma linguagem de programa√ß√£o? (sim/n√£o)", "resposta": "sim"},
    {"pergunta": "Qual √© o planeta mais pr√≥ximo do Sol?", "resposta": "Merc√∫rio"},
    {"pergunta": "Quem √© o autor do romance Romeu e Julieta?", "resposta": "William Shakespeare"},
    {"pergunta": "Qual √© o elemento qu√≠mico com s√≠mbolo H?", "resposta": "Hidrog√™nio"}
]

utilizador = {"nome": "Jogador"}
historico = []

def configurar_utilizador():
    nome = input("Digite seu nome: ")
    utilizador["nome"] = nome
    print(f"Bem-vindo(a), {nome}!")

def jogar_quiz():
    print(f"\nüéÆ Iniciando o jogo para {utilizador['nome']}...\n")
    perguntas_sorteadas = random.sample(perguntas, k=len(perguntas))

    for p in perguntas_sorteadas:
        print("‚ùì", p["pergunta"])
        resposta = input("Resposta: ").strip()
        acertou = resposta.lower() == p["resposta"].lower()
        resultado = "‚úÖ Correto!" if acertou else f"‚ùå Errado! Resposta certa: {p['resposta']}"
        print(resultado + "\n")

        historico.append({
            "pergunta": p["pergunta"],
            "resposta_user": resposta,
            "correta": p["resposta"],
            "acertou": acertou
        })
def mostrar_estatisticas():
    total = len(historico)
    acertos = sum(1 for h in historico if h["acertou"])
    erros = total - acertos
    print(f"\nüìä Estat√≠sticas de {utilizador['nome']}:")
    print(f"- Total de perguntas respondidas: {total}")
    print(f"- Acertos: {acertos}")
    print(f"- Erros: {erros}")

    print("\nüìò Hist√≥rico de respostas:")
    for h in historico:
        status = "‚úÖ" if h["acertou"] else "‚ùå"
        print(f"{status} Pergunta: {h['pergunta']}")
        print(f"   Sua resposta: {h['resposta_user']} | Correta: {h['correta']}\n")
def menu():
    while True:
        print("\n=== MENU ===")
        print("1. Configurar utilizador")
        print("2. Jogar")
        print("3. Ver estat√≠sticas")
        print("4. Sair")

